<?php

/*
|--------------------------------------------------------------------------
| Get The Session Configuration
|--------------------------------------------------------------------------
*/

$sessionConfig = require __DIR__ . '/../config/session.php';

/*
|--------------------------------------------------------------------------
| Start The Session
|--------------------------------------------------------------------------
*/

$app->make('session')->start(
    $sessionConfig['driver'],
    $sessionConfig['name'],
    $sessionConfig['duration']
);

/*
|--------------------------------------------------------------------------
| Clean Up
|--------------------------------------------------------------------------
*/

unset($sessionConfig);
