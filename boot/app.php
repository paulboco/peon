<?php

/*
|-------------------------------------------------------------------------------
| Create The Application
|-------------------------------------------------------------------------------
*/

$app = Peon\App::getInstance();

/*
|-------------------------------------------------------------------------------
| Set The Root Path
|-------------------------------------------------------------------------------
*/

$app->setRootPath(realpath(__DIR__.'/../'));

/*
|-------------------------------------------------------------------------------
| Register Configured Services
|-------------------------------------------------------------------------------
*/

$app->registerServices();

/*
|-------------------------------------------------------------------------------
| Check For Maintenance Mode
|-------------------------------------------------------------------------------
*/

if ($app->inMaintenance()) {
    $app->showMaintenance();
}

return $app;
