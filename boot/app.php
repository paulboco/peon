<?php

/*
|-------------------------------------------------------------------------------
| Create The Application
|-------------------------------------------------------------------------------
*/

$app = new Peon\Application\App;

/*
|-------------------------------------------------------------------------------
| Set The Root Path
|-------------------------------------------------------------------------------
*/

$app->setRootPath(realpath(__DIR__ . '/../'));

/*
|-------------------------------------------------------------------------------
| Register Configured Bindings
|-------------------------------------------------------------------------------
*/

$app->registerBindings(require __DIR__ . '/../config/bindings.php');

/*
|-------------------------------------------------------------------------------
| Check For Maintenance Mode
|-------------------------------------------------------------------------------
*/

if ($app->inMaintenance()) {
    $app->showMaintenance();
}

return $app;
